package com.class21_HW;

public class Multiplication {
static void mult (int a, int b) {
	System.out.println("The multiplication total is "+(a*b));
}
static void mult (int a, int b, int c) {
	System.out.println("The multiplication total is "+(a*b*c));
}
static void mult (int a, int b,int c, int d) {
	System.out.println("The multiplication total is "+(a*b*c*d));
}
}
