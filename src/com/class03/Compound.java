package com.class03;

public class Compound {

	public static void main(String[] args) {
		int num=200;
		num=num+300;
		System.out.println(num);
		num-=100;//num=num-100
		System.out.println(num);
		num+=50;//num=num+50
		System.out.println(num);
		num*=50;
		System.out.println(num);
		num/=50;
		System.out.println(num);
	}
}
