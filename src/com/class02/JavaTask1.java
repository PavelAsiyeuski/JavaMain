package com.class02;

public class JavaTask1 {

	public static void main(String[] args) {
		
		double num1=10.50;
		double num2=5.50;
		
		double sum1=num1+num2;
		double sub1=num1-num2;
		double mult1=num1*num2;
		double div1=num1/num2;
		
		System.out.println("The addition of 2 numbers "+num1+" and "+num2+" is equal to "+sum1);
		System.out.println("The subtraction of 2 numbers "+num1+" and "+num2+" is equal to "+sub1);
		System.out.println("The multiplication of 2 numbers "+num1+" and "+num2+" is equal to "+mult1);
		System.out.println("The division of 2 numbers "+num1+" and "+num2+" is equal to "+div1);

	}

}
