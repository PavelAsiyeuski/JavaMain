package com.class01;

public class PracticeVariables {

	public static void main(String [] args) {
		//8 primitive datatypes
		
	
		byte variableOne=10;
		short variableTwo=100;
		int variableThree=10000;
		long variableFour=10000000;
		
		//to store decimal numbers --> float,double
		
		float variableFive=12.99f;
		double variableSix=132.89;
		
		//to store single character -->
		char variableSeven='F';
		
		//to store boolean -->
		boolean hungry=true;
		boolean thirsty=false;
		
		System.out.println(variableOne);
		System.out.println(variableTwo);
		System.out.println(variableThree);
		System.out.println(variableFour);
		System.out.println(variableFive);
		System.out.println(variableSix);
		System.out.println(variableSeven);
		System.out.println(thirsty);
		System.out.println(hungry);
	}
}
